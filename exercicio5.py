""" Exibe uma vogal de acordo com a opção escolhida"""

r = str()
entrada = str(input("Digite um valor: b,f,j,p,z: "))

r = entrada

#Caso o valor digitado seja b apresentará a vogal a
if (r == "b"):
    b = "a"
    print(b)

#Caso o valor digitado seja f apresentará a vogal e
if (r == "f"):
    f = "e"
    print(f)

#Caso o valor digitado seja j apresentará a vogal i
if (r == "j"):
    j = "i"
    print(j)

#Caso o valor digitado seja p apresentará a vogal o
if (r == "p"):
    p = "o"
    print(p)

#Caso o valor digitado seja z apresentará a vogal u
if (r == "z"):
    z = "u"
    print(z)



